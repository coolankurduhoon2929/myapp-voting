const {mongoose}=require('./../db/mongoose');

var User=mongoose.model('User',{
  username:{
    type:String,
    trim:true,
    minlength:4,
    required:true,
    unique:true
  },
  firstname:{
    type:String,
    trim:true
  },
  lastname:{
    type:String,
    trim:true
  },
  city:{
    type:String
  },
  occupation:{
    type:String
  },
  password:{
    type:String,
    required:true,
    minlength:5
  },
  imgsrc:{
    type:String
  },
  age:{
    type:Number,
    min:0,
    max:120,
    required:true
  },
  about:{
    type:String,
    trim:true
  },
  gender:{
    type:String,
    required:true
  },
  tokens:[{
    access:{
      type:String
    },
    token:{
      type:String
    }
  }],
  questions:[{
    question:{type:String},
    qid:{type:String},
    option_selected:{type:String},
    pollDate:{type:Date}
  }],
  followers:[],
  following:[],
  dateJoined:{type:Date}
});

module.exports={User};
